CREATE TABLE cliente (
    id INTEGER AUTO_INCREMENT,
    edad INTEGER,
    genero VARCHAR(255),
    ingresos INTEGER,
    estado_civil VARCHAR(255),
    cantidad_hijos INTEGER,
    ocupacion VARCHAR(255),
    CONSTRAINT PK_cliente PRIMARY KEY (id)
);

CREATE TABLE tipo_pqrs (
    id INTEGER AUTO_INCREMENT,
    descripcion VARCHAR(255),
    CONSTRAINT PK_tipo_pqrs PRIMARY KEY (id)
);

CREATE TABLE pqrs (
    id INTEGER AUTO_INCREMENT,
    id_cliente INTEGER,
    id_tipo_pqrs INTEGER,
    fecha DATE,
    contenido VARCHAR(500),
    CONSTRAINT PK_pqrs PRIMARY KEY (id),
    CONSTRAINT FK_pqrs_cliente FOREIGN KEY (id_cliente) REFERENCES cliente(id),
    CONSTRAINT FK_pqrs_tipo_pqrs FOREIGN KEY (id_tipo_pqrs) REFERENCES tipo_pqrs(id)
);

CREATE TABLE encuesta (
    id INTEGER AUTO_INCREMENT,
    titulo VARCHAR(255),
    objetivo VARCHAR(255),
    fecha_inicio DATE,
    fecha_fin DATE,
    CONSTRAINT PK_encuesta PRIMARY KEY (id)
);

CREATE TABLE pregunta (
    id INTEGER AUTO_INCREMENT,
    id_encuesta INTEGER,
    contenido VARCHAR(255),
    CONSTRAINT PK_pregunta PRIMARY KEY (id),
    CONSTRAINT FK_pregunta_encuesta FOREIGN KEY (id_encuesta) REFERENCES encuesta(id)
);

CREATE TABLE respuesta (
    id_cliente INTEGER,
    id_pregunta INTEGER,
    contenido VARCHAR(255),
    CONSTRAINT PK_respuesta PRIMARY KEY (id_cliente, id_pregunta),
    CONSTRAINT FK_respuesta_cliente FOREIGN KEY (id_cliente) REFERENCES cliente(id),
    CONSTRAINT FK_respuesta_pregunta FOREIGN KEY (id_pregunta) REFERENCES pregunta(id)
);

CREATE TABLE red_social (
    id INTEGER AUTO_INCREMENT,
    nombre VARCHAR(255),
    seguidores INTEGER,
    CONSTRAINT PK_red_social PRIMARY KEY (id)
);

CREATE TABLE publicacion (
    id INTEGER AUTO_INCREMENT,
    id_red_social INTEGER,
    contenido VARCHAR(255),
    me_gusta INTEGER,
    CONSTRAINT PK_publicacion PRIMARY KEY (id),
    CONSTRAINT FK_publicacion_red_social FOREIGN KEY (id_red_social) REFERENCES red_social(id)
);

CREATE TABLE comentario (
    id INTEGER AUTO_INCREMENT,
    id_cliente INTEGER,
    id_publicacion INTEGER,
    contenido VARCHAR(255),
    me_gusta INTEGER,
    CONSTRAINT PK_comentario PRIMARY KEY (id),
    CONSTRAINT FK_comentario_cliente FOREIGN KEY (id_cliente) REFERENCES cliente(id),
    CONSTRAINT FK_comentario_publicacion FOREIGN KEY (id_publicacion) REFERENCES publicacion(id)
);

CREATE TABLE producto (
    id INTEGER AUTO_INCREMENT,
    nombre VARCHAR(255),
    precio INTEGER,
    CONSTRAINT PK_producto PRIMARY KEY (id)
);

CREATE TABLE resena (
    id_cliente INTEGER,
    id_producto INTEGER,
    fecha DATE,
    calificacion INTEGER,
    contenido VARCHAR(255),
    CONSTRAINT PK_resena PRIMARY KEY (id_cliente, id_producto),
    CONSTRAINT FK_resena_cliente FOREIGN KEY (id_cliente) REFERENCES cliente(id),
    CONSTRAINT FK_resena_producto FOREIGN KEY (id_producto) REFERENCES producto(id)
);

CREATE TABLE pedido (
    id INTEGER AUTO_INCREMENT,
    id_cliente INTEGER,
    id_producto INTEGER,
    cantidad INTEGER,
    fecha_hora DATETIME,
    CONSTRAINT PK_pedido PRIMARY KEY (id),
    CONSTRAINT FK_pedido_cliente FOREIGN KEY (id_cliente) REFERENCES cliente(id),
    CONSTRAINT FK_pedido_producto FOREIGN KEY (id_producto) REFERENCES producto(id)
);

CREATE TABLE proceso (
    id INTEGER AUTO_INCREMENT,
    id_producto INTEGER,
    nombre VARCHAR(255),
    CONSTRAINT PK_proceso PRIMARY KEY (id),
    CONSTRAINT FK_proceso_producto FOREIGN KEY (id_producto) REFERENCES producto(id)
);

CREATE TABLE resultado (
    id INTEGER AUTO_INCREMENT,
    descripcion VARCHAR(50),
    CONSTRAINT PK_resultado PRIMARY KEY (id)
);

CREATE TABLE registro (
    id INTEGER AUTO_INCREMENT,
    id_proceso INTEGER,
    id_resultado INTEGER,
    fecha_hora_inicio DATETIME,
    fecha_hora_fin DATETIME,
    anotaciones VARCHAR(255),
    CONSTRAINT PK_registro PRIMARY KEY (id),
    CONSTRAINT FK_registro_proceso FOREIGN KEY (id_proceso) REFERENCES proceso(id),
    CONSTRAINT FK_registro_resultado FOREIGN KEY (id_resultado) REFERENCES resultado(id)
   
);

CREATE TABLE recurso (
    id INTEGER AUTO_INCREMENT,
    nombre VARCHAR(255),
    CONSTRAINT PK_recurso PRIMARY KEY (id)
);

CREATE TABLE registro_recurso (
    id_registro INTEGER,
    id_recurso INTEGER,
    CONSTRAINT PK_registro_recurso PRIMARY KEY (id_registro, id_recurso),
    CONSTRAINT FK_registro_recurso_1 FOREIGN KEY (id_registro) REFERENCES registro(id),
    CONSTRAINT FK_registro_recurso_2 FOREIGN KEY (id_recurso) REFERENCES recurso(id)
);

CREATE TABLE empleado (
    id INTEGER AUTO_INCREMENT,
    nombre VARCHAR(255),
    edad INTEGER,
    genero VARCHAR(255),
    rol VARCHAR(255),
    CONSTRAINT PK_empleado PRIMARY KEY (id)
);

CREATE TABLE registro_empleado (
    id_registro INTEGER,
    id_empleado INTEGER,
    CONSTRAINT PK_registro_empleado PRIMARY KEY (id_registro, id_empleado),
    CONSTRAINT FK_registro_empleado_1 FOREIGN KEY (id_registro) REFERENCES registro(id),
    CONSTRAINT FK_registro_empleado_2 FOREIGN KEY (id_empleado) REFERENCES empleado(id)
);

